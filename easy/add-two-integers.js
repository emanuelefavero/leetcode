/**
 *  * Add Two Integers
 *
 * @param {number} num1 - first number
 * @param {number} num2 - second number
 * @return {number} The sum of the two numbers
 */

// O(1) time | O(1) space
export function sum(num1, num2) {
  return num1 + num2
}

// -------------------------
// TESTS

console.log(sum(1, 2)) // 3
console.log(sum(2, 3)) // 5
